import { DateTime } from 'luxon';

// Helper function to get user's timezone and combine with IANA list
export function getAvailableTimezones(): string[] {
  try {
    // Get current DateTime to calculate offsets
    const now = DateTime.now();
    
    // Get user's timezone
    const userTimezone = Intl.DateTimeFormat().resolvedOptions().timeZone;
    const timezones = userTimezone && !IANA_TIMEZONES.includes(userTimezone) 
      ? [userTimezone, ...IANA_TIMEZONES]
      : [...IANA_TIMEZONES];
    
    // Sort timezones by UTC offset
    return timezones.sort((a, b) => {
      const offsetA = DateTime.now().setZone(a).offset;
      const offsetB = DateTime.now().setZone(b).offset;
      return offsetA - offsetB;
    });
  } catch (error) {
    console.error('Error getting user timezone:', error);
    return [...IANA_TIMEZONES];
  }
}